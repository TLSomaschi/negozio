  id INT PRIMARY KEY AUTO_INCREMENT,
  cliente_id INT,
  prodotto_id INT,
  quantita INT,
  data_ordine DATE,
  FOREIGN KEY (cliente_id) REFERENCES clienti(id),
  FOREIGN KEY (prodotto_id) REFERENCES prodotti(id)
